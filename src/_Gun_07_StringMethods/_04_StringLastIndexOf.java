package _Gun_07_StringMethods;

public class _04_StringLastIndexOf {
    public static void main(String[] args) {
        //index of gibi calisir aama aramaya sondan baslar

        String s1="Merhaba";
        System.out.println("s1.indexOf(a)=" +s1.indexOf("a")); //4

        System.out.println("s1.lastIndexOf(a)=" +s1.lastIndexOf("a")); //6
        //index her zaman aynidir ,sadece arama yönü degisiyor

    }
}
