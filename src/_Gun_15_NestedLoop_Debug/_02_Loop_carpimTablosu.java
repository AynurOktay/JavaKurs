package _Gun_15_NestedLoop_Debug;

public class _02_Loop_carpimTablosu {
    public static void main(String[] args) {

        //10lara kadar carpim tablosunu tabela seklinde yazdirin



        for (int i=1; i<=10;i++){

            for (int j=1 ; j<=10 ; j++){

                System.out.println(i +"x" +j +"=" + (i*j));
            }

            System.out.println();

        }


    }
}
